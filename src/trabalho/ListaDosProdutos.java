    public void PreencherLinhaTabela()
    {
        DefaultTableModel tModel = (DefaultTableModel) tblProdutos.getModel();
        Produtos prod;
       
        Object linha[] = new Object[5];
        
        for (int i = 0; i < listaProduto.size(); i++) 
        {
         
 
                prod = (Produtos)listaProduto.get(i);
                linha[0] = prod.getCodigoDeBarras();
                linha[1] = prod.getNome();
                linha[2] = prod.getUnidade();
                linha[3] = prod.getTipo();
                linha[4] = prod.getValorAVista();
            
            
            
            

            tModel.addRow(linha);
        }
    }

